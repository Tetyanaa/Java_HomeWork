package com.tplysyuk.module3;

public class Operators {
    public int addition(int firstNum, int secNum) {
        return firstNum + secNum;
    }

    public int subtraction(int firstNum, int secNum) {
        return firstNum - secNum;
    }

    public int multiplication(int firstNum, int secNum) {
        return firstNum * secNum;
    }

    public int divisions(int firstNum, int secNum) {
        return firstNum / secNum;
    }

    public int mod(int firstNum, int secNum) {
        return firstNum % secNum;
    }

    public int multidiv(int firstNum, int secNum, int thirdNum) {
        return firstNum * secNum / thirdNum;
    }

    public int divmulti(int firstNum, int secNum, int thirdNum) {
        return firstNum / secNum * thirdNum;
    }

}

